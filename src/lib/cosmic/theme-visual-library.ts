// Map normalized theme names to their Visual DNA keys
export function getThemeVisualDna(themeName: string): VisualDna {
  const normalized = themeName
    .toLowerCase()
    .normalize("NFD")
    .replace(/[\u0300-\u036f]/g, "")
    .trim();

  if (normalized.includes("familia")) return themeVisualLibrary.familia;
  if (normalized.includes("emocoes") || normalized.includes("emocao")) return themeVisualLibrary.emocoes;
  if (normalized.includes("ansiedade") || normalized.includes("calma")) return themeVisualLibrary.ansiedade;
  if (normalized.includes("prosperidade") || normalized.includes("abundancia") || normalized.includes("dinheiro")) return themeVisualLibrary.prosperidade;
  if (normalized.includes("autocuidado") || normalized.includes("cuidado")) return themeVisualLibrary.autocuidado;
  if (normalized.includes("limite")) return themeVisualLibrary.limites;
  if (normalized.includes("merecimento")) return themeVisualLibrary.merecimento;
  if (normalized.includes("espiritualidade") || normalized.includes("conexao")) return themeVisualLibrary.espiritualidade;
  if (normalized.includes("relacionamento")) return themeVisualLibrary.relacionamentos;
  if (normalized.includes("carreira") || normalized.includes("trabalho")) return themeVisualLibrary.carreira;
  if (normalized.includes("autoestima") || normalized.includes("amor proprio")) return themeVisualLibrary.autoestima;
  if (normalized.includes("proposito")) return themeVisualLibrary.proposito;

  return themeVisualLibrary.fallback;
}

// Generate rich sensory details based on the theme key and selected values
export function getSensoryDetails(themeName: string, environment: string, mood: string): string {
  const normalized = themeName
    .toLowerCase()
    .normalize("NFD")
    .replace(/[\u0300-\u036f]/g, "")
    .trim();

  if (normalized.includes("familia")) {
    return "warm textures of wood and fabrics, soft glowing morning light, cozy emotional warmth of belonging, feeling of safety and ancestral connection";
  }
  if (normalized.includes("emocoes") || normalized.includes("emocao")) {
    return "liquid textures, gentle ripples on water, cool silver and blue light, shifting emotional temperature, deep introspective feeling of flowing water";
  }
  if (normalized.includes("ansiedade") || normalized.includes("calma")) {
    return "misty air textures, soft clearing sunlight, cooling calming temperature, feeling of space expanding and breathing deeply";
  }
  if (normalized.includes("prosperidade") || normalized.includes("abundancia") || normalized.includes("dinheiro")) {
    return "luminous golden particles, warm radiant portal glow, welcoming and expanding emotional temperature, feeling of abundance and sacred space";
  }
  if (normalized.includes("autocuidado") || normalized.includes("cuidado")) {
    return "soft fabrics, velvet petals, warm steam, gentle comforting morning sunlight, warm tender emotional temperature, feeling of peaceful solitude";
  }
  if (normalized.includes("limite")) {
    return "firm protective boundaries, soft violet and blue glow, cool serene temperature, feeling of quiet safety and focused inner strength";
  }
  if (normalized.includes("merecimento")) {
    return "shimmering pearl and gold textures, soft celestial downpour of light, warm inviting emotional temperature, feeling of dignity and expanding heart";
  }
  if (normalized.includes("espiritualidade") || normalized.includes("conexao")) {
    return "starry sparkles, sacred geometric light paths, cool cosmic night breeze, deep transcendental emotional temperature, feeling of vastness and inner flame";
  }
  if (normalized.includes("relacionamento")) {
    return "soft thread textures, warm glow between hands, warm gentle sunset light, vulnerable and honest emotional temperature, feeling of connection and mutual respect";
  }
  if (normalized.includes("carreira") || normalized.includes("trabalho")) {
    return "sharp clean textures, focused sunlight path, clear crisp morning air temperature, motivated and determined emotional temperature, feeling of clear direction and climbing";
  }
  if (normalized.includes("autoestima") || normalized.includes("amor proprio")) {
    return "reflective mirror surfaces, warm golden aura glow, soft rose gold light, compassionate and self-respecting emotional temperature, feeling of being seen and valued";
  }
  if (normalized.includes("proposito")) {
    return "glowing compass needle, trail of starlight, fresh dawn air temperature, courageous and aligned emotional temperature, feeling of following a calling";
  }

  return "balanced natural textures, soft ambient lighting, comfortable stable temperature, feeling of inner peace and alignment";
}
